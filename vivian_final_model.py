    X_te  = X_test_base.copy()

    y_tr  = y_train_all[tr_idx]
    y_val = y_train_all[val_idx]

    # Target-encode all categorical columns inside fold
    for col in CATEGORICAL_ENCODE_COLS:
        if col in train_df.columns:
            enc_col = f'{col}_fpd_rate'
            X_tr[enc_col], X_val[enc_col], X_te[enc_col] = target_encode_fold(
                tr_df_fold, val_df_fold, test_df, col
            )

    # Fill NaN with training fold median
    for col in X_tr.columns:
        med = X_tr[col].median()
        X_tr[col]  = X_tr[col].fillna(med)
        X_val[col] = X_val[col].fillna(med)
        X_te[col]  = X_te[col].fillna(med)

    # Train LightGBM
    params = {
        'objective':         'binary',
        'metric':            'auc',
        'learning_rate':     0.05,
        'num_leaves':        63,
        'min_child_samples': 50,
        'feature_fraction':  0.8,
        'bagging_fraction':  0.8,
        'bagging_freq':      5,
        'scale_pos_weight':  scale_pos_weight,
        'verbose':           -1,
        'n_jobs':            -1,
    }
    dtrain = lgb.Dataset(X_tr, label=y_tr)
    dval   = lgb.Dataset(X_val, label=y_val, reference=dtrain)
    model  = lgb.train(
        params, dtrain,
        num_boost_round=1000,
        valid_sets=[dval],
        callbacks=[lgb.early_stopping(50, verbose=False), lgb.log_evaluation(False)]
    )

    val_pred  = model.predict(X_val)
    test_pred = model.predict(X_te)

    auc = roc_auc_score(y_val, val_pred)
    fold_aucs.append(auc)
    oof_preds[val_idx] = val_pred
    test_preds        += test_pred / 5
    print(f"  Fold {fold} AUC: {auc:.4f}  trees={model.best_iteration}")

